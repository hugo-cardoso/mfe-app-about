      módulos em uma única aplicação no momento em que a página é carregada pelo
      usuário, em vez de incluir todos os módulos em um único build. Isso
      permite que os módulos sejam atualizados e implanta-dos de forma
      independente, o que facilita a manutenção e o desenvolvimento contínuo da
